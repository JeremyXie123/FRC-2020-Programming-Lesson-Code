public class SwitchCase {
    public static void main(String[] args) {
        int num1 = 5;
        switch(num1) {
            case 1:
                // if num1 is equal to 1
              break;
            case 2:
                // if num1 is equal to 2
              break;
            case 3:
                // if num1 is equal to 3
              break;
            default:
                // otherwise
        } 
    }
}
